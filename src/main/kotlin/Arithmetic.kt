import kotlin.math.*
import kotlin.math.PI

fun main() {
    val number1 = 10
    val number2 = 2
    println("+ ${number1 + number2}")
    println("- ${number1 - number2}")
    println("* ${number1 * number2}")
    println("/ ${number1 / number2}")
    println("% ${number1 % number2}")

    // math
    var doubleVariable = 456.1345
    println(PI)
    println(round(doubleVariable))
    println(max(number1, number2))
    println(min(number1, number2))
    println(sqrt(36.0))
    println(kotlin.math.ceil(36.1))
}